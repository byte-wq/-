//

#include <iostream>
#include <vector>
#include <algorithm>

using namespace std;

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int N;
    cin >> N;

    vector<pair<int, int>> a(N); // (start, finish)
    for (int i = 0; i < N; i++) {
        cin >> a[i].first >> a[i].second;
    }

    // Сортируем по времени окончания (fi), это стандартный greedy для интервалов
    sort(a.begin(), a.end(), [](const auto& x, const auto& y) {
        if (x.second != y.second) return x.second < y.second;
        return x.first < y.first; // если одинаковое окончание, раньше начатую ставим первой (не критично)
        });

    int cnt = 0;          // сколько лекций получилось поставить
    int lastFinish = 0;   // время окончания последней выбранной лекции

    for (int i = 0; i < N; i++) {
        int s = a[i].first;
        int f = a[i].second;

        // Условие для полуинтервалов: можно если s >= lastFinish
        if (s >= lastFinish) {
            cnt++;             // берём лекцию
            lastFinish = f;    // обновляем конец расписания
        }
    }

    cout << cnt << "\n";
    return 0;
}


#include <iostream>   
#include <fstream>   
#include <set>        

using namespace std;

int main() {
    setlocale(LC_ALL, "Russian");
    ifstream fin("input.txt");   // открываем входной файл
    ofstream fout("output.txt"); // открываем выходной файл

    if (!fin.is_open() || !fout.is_open()) {  
        cerr << "Ошибка при открытии файла!" << endl;
        return 1;
    }

    set<int> s;  // множество чисел 
    int x;

  
    while (fin >> x) {
        if (x > 0) {
            s.insert(x);// добавляем число
        }
        else if (x < 0) {
            s.erase(-x);// удаляем противоположное
        }
        else { 
            bool first = true; // если встретили 0  выводим результат
            for (int value : s) {
                if (!first) fout << ' ';
                fout << value;
                first = false;
            }
            fout << '\n';
            break; 
        }
    }

    fin.close();
    fout.close();
    return 0;
}


